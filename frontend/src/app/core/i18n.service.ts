@Injectable({ providedIn: 'root' })
export class I18nService {
  readonly language = signal<Language>(this.initialLanguage());

  toggleLanguage(): void {
    this.setLanguage(this.language() === 'fr' ? 'en' : 'fr');
  }

  setLanguage(language: Language): void {
    this.language.set(language);
    localStorage.setItem('language', language);
    document.documentElement.lang = language;
  }

  translate(key: string): string {
    return TRANSLATIONS[this.language()][key] ?? key;
  }

  localized(fr = '', en = ''): string {
    return this.language() === 'fr' ? fr || en : en || fr;
  }

  inlineLocalized(value: unknown): string {
    if (typeof value !== 'string') {
      return '';
    }

    const parts = value.split(/\s+\/\s+/);
    if (parts.length < 2) {
      return value;
    }

    return this.language() === 'fr' ? parts[0] : parts.slice(1).join(' / ');
  }

  private initialLanguage(): Language {
    const stored = localStorage.getItem('language');
    if (stored === 'fr' || stored === 'en') {
      document.documentElement.lang = stored;
      return stored;
    }
    document.documentElement.lang = 'fr';
    return 'fr';
  }
}
